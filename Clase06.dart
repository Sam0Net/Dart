// Hay algunos caracteres que no podemos utilizar en un String un ejemplo de este sería el signo $ para utilizarlo de manera literal en un String debemos hacer uso de \
void main() {
  // Escapar Caracteres
  print("Tu saldo es \'\$100'");
  // Salto de línea 1ra forma.
  print("Bienvenido \n programador");
  // Salto de línea 2da forma.
  print('''
  Hoy aprenderás sobre
  el leguaje de programación
  Dart.
  ''');
}
