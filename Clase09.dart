// Operadores ariméticos
void main() {
  int a = 20;
  int b = 17;
  // Operador de suma
  int c = a + b;
  print(c);
  // Operador de resta
  int d = a - b;
  print(d);
  // Operador de multiplicación
  int e = a * b;
  print(e);
  // Operador de división
  int f = a ~/ b;
  print(f);
  // Obtener residuo de una división
  int g = a % b;
  print(g);
}
