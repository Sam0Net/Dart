// Operadores de comparación
void main() {
  int edadMax = 25;
  int edadMin = 18;
  int edad = 20;
  String pase = "Si";
  // Operador de comparación mayor que...
  print(edad > edadMin);
  // Operador de comparación menor que...
  print(edad < edadMax);
  // Operador de comparación mayor igual que...
  print(edad >= edadMin);
  // Operador de comparación menor igual que...
  print(edad <= edadMax);
  // Operador de comparación igual
  print(edad == edadMin);
  print(pase == "Si");
}
